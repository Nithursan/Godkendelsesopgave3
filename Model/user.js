class User {
    constructor(firstname, lastname, age, city, bio, password, image, interest) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.age = age;
        this.city = city;
        this.bio = bio;
        this.password = password;
        this.image = image;
        this.interest = interest;

    }
}

module.exports = User;